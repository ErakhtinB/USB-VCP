/* USER CODE BEGIN 4 */
void StartSaveTask(void const * argument)
{
	char saved_msg[] = "SAVED!\r\n";
	for (;;)
	{
		xSemaphoreTake(save_semaphore, portMAX_DELAY);
		
		//wait for access
		xSemaphoreTake(array_semaphore, portMAX_DELAY);
		
		FLASH_Write();
		
		//transmit success msg
		CDC_Transmit_FS((uint8_t *) saved_msg, sizeof(saved_msg) - 1);
		
		//give access
		xSemaphoreGive(array_semaphore);
	}
}

void FLASH_Read(void)
{
  //Read settings
  uint32_t *source_addr = (uint32_t *) MY_FLASH_PAGE_ADDR;
	flash_buffer = *source_addr;
	memcpy(brightness_percentage, &flash_buffer, sizeof(brightness_percentage));
}
 
void FLASH_Write(void)
{
	memcpy(&flash_buffer, brightness_percentage, sizeof(flash_buffer));
	HAL_FLASH_Unlock();
  __HAL_FLASH_CLEAR_FLAG(FLASH_FLAG_EOP | FLASH_FLAG_OPERR | FLASH_FLAG_WRPERR | FLASH_FLAG_PGAERR | FLASH_FLAG_PGSERR );
  FLASH_Erase_Sector(FLASH_SECTOR_11, VOLTAGE_RANGE_3);
  HAL_FLASH_Program(TYPEPROGRAM_WORD, MY_FLASH_PAGE_ADDR, flash_buffer);
  HAL_FLASH_Lock();
}

void set_brightness(void)
{
	TIM4->CCR1 = (COUNTER_PERIOD + 1) * brightness_percentage[0] / 100;
	TIM4->CCR2 = (COUNTER_PERIOD + 1) * brightness_percentage[1] / 100;
	TIM4->CCR3 = (COUNTER_PERIOD + 1) * brightness_percentage[2] / 100;
	TIM4->CCR4 = (COUNTER_PERIOD + 1) * brightness_percentage[3] / 100;
}
			
/* USER CODE END 4 */

/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{
  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 5 */	
	//buffer for queue
	uint8_t brightness_percentage_buffer[4];
	
	//msg to transmit when brigntness is successfully changed
	char ok_msg[] = "OK!\r\n";
	
  /* Infinite loop */
  for(;;)
  {
		//wait for queue
		xQueueReceive(msgQueueHandle, brightness_percentage_buffer, portMAX_DELAY);
		
		//wait for access
		xSemaphoreTake(array_semaphore, portMAX_DELAY);
		
		//copy from buffer
		memcpy(brightness_percentage, brightness_percentage_buffer, sizeof(brightness_percentage));

		//change duty cycle
		set_brightness();
		
		//transmit success msg
		CDC_Transmit_FS((uint8_t *) ok_msg, sizeof(ok_msg) - 1);
		
		//give access
		xSemaphoreGive(array_semaphore);
  }
  /* USER CODE END 5 */ 
}

